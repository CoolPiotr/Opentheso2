package fr.cnrs.opentheso.bean.toolbox.atelier;


public class ConceptResultNode {
    
    private String idOrigine;
    private String prefLabelOrigine;
    private String prefLabelConcept;
    private String altLabelConcept;
    private String idConcept;
    private String uriArk;
    private String definition;
    private String termGenerique;

    public String getIdOrigine() {
        return idOrigine;
    }

    public void setIdOrigine(String idOrigine) {
        this.idOrigine = idOrigine;
    }

    public String getPrefLabelOrigine() {
        return prefLabelOrigine;
    }

    public void setPrefLabelOrigine(String prefLabelOrigine) {
        this.prefLabelOrigine = prefLabelOrigine;
    }

    public String getPrefLabelConcept() {
        return prefLabelConcept;
    }

    public void setPrefLabelConcept(String prefLabelConcept) {
        this.prefLabelConcept = prefLabelConcept;
    }

    public String getAltLabelConcept() {
        return altLabelConcept;
    }

    public void setAltLabelConcept(String altLabelConcept) {
        this.altLabelConcept = altLabelConcept;
    }

    public String getIdConcept() {
        return idConcept;
    }

    public void setIdConcept(String idConcept) {
        this.idConcept = idConcept;
    }

    public String getUriArk() {
        return uriArk;
    }

    public void setUriArk(String uriArk) {
        this.uriArk = uriArk;
    }

    public String getDefinition() {
        return definition;
    }

    public void setDefinition(String definition) {
        this.definition = definition;
    }

    public String getTermGenerique() {
        return termGenerique;
    }

    public void setTermGenerique(String termGenerique) {
        this.termGenerique = termGenerique;
    }
    
}